void main(List<String> args) {
  //unary postfix operator
  int age = 40;
  print(age--); // it will print 40 and then decrease the value
  print(age); // it will print 39, because it is already decreased
  print(age++); // it will print 39 and then increase the value
  print(age); // it will print 40, because it is already increased
}
